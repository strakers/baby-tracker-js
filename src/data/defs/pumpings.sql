CREATE TABLE IF NOT EXISTS pumpings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uuid UUID NOT NULL,
  started_at TIMESTAMP NOT NULL,
  ended_at TIMESTAMP NOT NULL, 
  duration INTEGER NOT NULL,
  left_amount INTEGER NOT NULL,
  right_amount INTEGER NOT NULL,
  note TEXT NULL,
  
  baby_id BIGINT REFERENCES babies ON DELETE CASCADE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  modified_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TRIGGER update_pumpings_modtime BEFORE UPDATE ON pumpings FOR EACH ROW EXECUTE PROCEDURE  update_modified_column();