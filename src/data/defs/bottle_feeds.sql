CREATE TABLE IF NOT EXISTS bottle_feeds (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uuid UUID NOT NULL,
  started_at TIMESTAMP NOT NULL,
  ended_at TIMESTAMP NOT NULL, 
  duration INTEGER NOT NULL,
  start_amount INT NOT NULL,
  finish_amount INT NOT NULL,
  note TEXT NULL,

  baby_id BIGINT REFERENCES babies ON DELETE CASCADE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  modified_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TRIGGER update_bottle_feeds_modtime BEFORE UPDATE ON bottle_feeds FOR EACH ROW EXECUTE PROCEDURE  update_modified_column();