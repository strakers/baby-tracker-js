CREATE TABLE IF NOT EXISTS nursings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uuid UUID NOT NULL,
  started_at TIMESTAMP NOT NULL,
  ended_at TIMESTAMP NOT NULL, 
  duration INTEGER NOT NULL,
  nursing_side nursing_sides NOT NULL,
  finish_amount INT NOT NULL,
  note TEXT NULL,
  
  baby_id BIGINT REFERENCES babies ON DELETE CASCADE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  modified_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TRIGGER update_nursings_modtime BEFORE UPDATE ON nursings FOR EACH ROW EXECUTE PROCEDURE  update_modified_column();